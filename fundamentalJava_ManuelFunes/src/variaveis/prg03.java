package variaveis;

/*
 * PRG03.java
 * Mostrando alguns tipos de variaveis
 */

public class prg03 {

	public static void main(String[] args) {

		// variaveis
		int idade = 10;
		float altura = 1.75f;
		double dist_terra_lua = 8.44d;
		char letra = 'A';

		// saída
		System.out.println("Int = " + idade + "\n");
		System.out.println("float = " + altura + "\n");
		System.out.println("double = " + dist_terra_lua + "\n");
		System.out.println("char = " + letra + "\n");

	}

}
