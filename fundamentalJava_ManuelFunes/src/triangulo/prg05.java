package triangulo;

/*
 * Calculando e mostrando a area de um triangulo
 * Area = (base * altura)/2 
 */

public class prg05 {

	public static void main(String[] args) {

		float base = 10f;
		float altura = 30f;
		float area = 0f;

		area = (base * altura) / 2;
		System.out.println("Area = " + area + "\n");

	}

}
